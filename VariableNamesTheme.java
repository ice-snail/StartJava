class VariableNamesTheme {
    public static void main(String [] args) {
        // Начало блока разные переменные
        System.out.println("1. Разные переменные");

        // Цифра
        int digit = 7;
        System.out.println("Цифра " + digit);

        // Сумма чисел
        int numsSum = 10;
        System.out.println("Сумма чисел = " + numsSum);

        // Произведение цифр
        int digitsProduct = digit * digit;
        System.out.println("Произведение цифр = " + digitsProduct);

        // Максимальное число
        int maxNum = 999;
        System.out.println("Максимальное число = " + maxNum);

        // Количество десятков
        int tenCount = 8;
        System.out.println("Количество десятков = " + tenCount);

        // Вес собаки
        int dogWeight = 4;
        System.out.println("Вес собаки = " + dogWeight + " кг");

        // Исходное число
        int originNum = 1;
        System.out.println("Исходное число = " + originNum);

        // Процент по вкладу
        int depositPercent = 11;
        System.out.println("Процент по вкладу = " + depositPercent + " %");

        // Переменная хранит символ &
        int symbol = '&';
        System.out.println("Переменная хранит символ - " + symbol);

        // Код ошибки
        int errorCode = 404;
        System.out.println("Код ошибки = " + errorCode);

        // Тип сообщения
        String typeOfMessage = "СПАМ";
        System.out.println("Тип сообщения - " + typeOfMessage);

        // Число нулей
        int zerosCount = 12;
        System.out.println("Число нулей = " + zerosCount);

        // Уникальное число
        int uniqueNum = 47;
        System.out.println("Уникальное число = " + uniqueNum);

        // Случайное число
        int randomNum = 29;
        System.out.println("Случайное число = " + randomNum);

        // Математическое выражение
        String mathExpression = "2 + 2";
        System.out.println("Математическое выражение - " + mathExpression);

        // Счёт в игре
        String gameScore = "7:4";
        System.out.println("Счёт в игре - " + gameScore);

        // Максимальная длина
        int maxLength = 4;
        System.out.println("Максимальная длина = " + maxLength + " метра");

        // Пункт меню
        String menuItem = "Контакты";
        System.out.println("Пункт меню = " + menuItem);

        // Стоимость кофе
        int coffeePrice = 200;
        System.out.println("Стоимость кофе = " + coffeePrice + " рублей");

        // Дата начала
        String startDate = "14.10.2024";
        System.out.println("Дата начала - " + startDate);

        // Окончание диапазона
        double endOfRange = 0.99;
        System.out.println("Окончание диапазона = " + endOfRange);

        // Полное имя работника месяца
        String fullNameOfTheBestWorker = "Иванов Иван Иванович";
        System.out.println("Полное имя работника месяца - " + fullNameOfTheBestWorker);

        // Заголовок электронной книги
        String ebookTitle = "Рассвет";
        System.out.println("Заголовок электронной книги - " + ebookTitle);

        // Размер
        int size = 46;
        System.out.println("Размер = " + size);

        // Вместимость
        double volume = 1.2;
        System.out.println("Вместимость = " + volume + " квадратных метра");

        // Счетчик
        int counter = 17;
        System.out.println("Счетчик = " + counter);

        // Путь до файла
        String path = "C:\\Program Files\\Git\\git-cmd.exe";
        System.out.println("Путь до файла " + path);

        // Количество чисел в каждой строке
        int countOfNumsInPerRow = 20;
        System.out.println("Количество чисел в каждой строке = " + countOfNumsInPerRow + "\n");

        // Начало блока Boolean-переменные
        System.out.println("2. Boolean-переменные");

        // Сотни равны?
        boolean isEqualsHundres = true;
        System.out.println("Сотни равны? - " + isEqualsHundres);

        // Компьютер включен?
        boolean isComputerOn = true;
        System.out.println("Компьютер включен? - " + isComputerOn);

        // Есть равные цифры?
        boolean hasEqualsDigits = false;
        System.out.println("Есть равные цифры? - " + hasEqualsDigits);

        // Создано?
        boolean isCreated = true;
        System.out.println("Создано? - " + isCreated);

        // Пустое?
        boolean empty = false;
        System.out.println("Пустое? - " + empty);

        // Активное?
        boolean active = true;
        System.out.println("Активное? - " + active);

        // Новое?
        boolean isNew = false;
        System.out.println("Новое? - " + isNew);

        // Электронная почта действительная?
        boolean isEmailValid = true;
        System.out.println("Электронная почта действительна? - " + isEmailValid);

        // Имеются уникальные строки?
        boolean hasUniqueRows = false;
        System.out.println("Имеются уникальные строки? - " + hasUniqueRows + "\n");

        // Начало блока аббревиатуры
        System.out.println("3. Аббревиатуры");

        // Старый universally unique identifier
        String oldUuid = "UUID";
        System.out.println("Старый universally unique identifier - " + oldUuid);

        // Производитель оперативной памяти
        String manufacturerRam = "Crucial";
        System.out.println("Производитель оперативной памяти - " + manufacturerRam);

        // Емкость жесткого диска
        String volumeHdd = "HHD";
        System.out.println("Емкость жесткого диска - " + volumeHdd);

        // Протокол передачи гипертекста
        String http = "HTTP";
        System.out.println("Протокол передачи гипертекста - " + http);

        // Сокращенный uniform resource locator
        String shortUrl = "URL";
        System.out.println("Сокращенный uniform resource locator - " + shortUrl);

        // Новый идентификатор клиента
        String newCuid = "CUID";
        System.out.println("Новый идентификатор клиента " + newCuid);

        // American standard code for information interchange
        String ascii = "ASCII";
        System.out.println("American standard code for information interchange - " + ascii);
    }
}